def clean_text(value: str) -> str:
    value = value.replace("\u00a0", " ")
    value = re.sub(r"\s+", " ", value)
    return value.strip()


def slug_id(value: str) -> str:
    value = value.lower()
    value = re.sub(r"[^a-z0-9α-ωάέήίόύώϊϋΐΰ]+", "-", value, flags=re.IGNORECASE)
    return value.strip("-") or "section"


def extract_pptx_slides(path: Path) -> list[dict[str, Any]]:
    ns = {"a": "http://schemas.openxmlformats.org/drawingml/2006/main"}
    with ZipFile(path) as archive:
        slide_names = [
            name
            for name in archive.namelist()
            if re.match(r"ppt/slides/slide\d+\.xml$", name)
        ]
        slide_names.sort(key=lambda name: int(re.search(r"slide(\d+)\.xml$", name).group(1)))
        slides: list[dict[str, Any]] = []
        for index, name in enumerate(slide_names, 1):
            tree = ET.fromstring(archive.read(name))
            parts = [
                node.text
                for node in tree.findall(".//a:t", ns)
                if node.text and node.text.strip()
            ]
            text = clean_text(" ".join(parts))
            title = text[:95] + ("..." if len(text) > 95 else "")
            slides.append({"number": index, "title": title, "text": text})
    return slides


def pdf_page_count(path: Path) -> int | None:
    if PdfReader is None or not path.exists():
        return None
    try:
        return len(PdfReader(str(path)).pages)
    except Exception:
        return None


