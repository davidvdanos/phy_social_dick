def render_notes(topic: dict[str, Any]) -> str:
    sections = []
    sections.append(
        """
        <section class="note-section" id="analytical-notes">
          <p class="eyebrow">Αναλυτικές σημειώσεις</p>
          <h2>Ροή μαθήματος</h2>
          <p>Οι παρακάτω σημειώσεις κρατούν τη λογική των διαλέξεων: πρώτα το βασικό πλαίσιο, μετά τα επιμέρους θεωρητικά σημεία και στο τέλος η κριτική ανάγνωση.</p>
        </section>
        """
    )
    for section in topic["sections"]:
        section_id = slug_id(section["heading"])
        paragraphs = "".join(f"<p>{h(study_text(paragraph))}</p>" for paragraph in section["paragraphs"])
        bullets = "".join(f"<li>{h(study_text(item))}</li>" for item in section.get("bullets", []))
        bullet_html = f"<ul>{bullets}</ul>" if bullets else ""
        sections.append(
            f"""
            <section class="note-section" id="{h(section_id)}">
              <h2>{h(section["heading"])}</h2>
              {paragraphs}
              {bullet_html}
            </section>
            """
        )
    return "\n".join(sections)


