def make_questions(topic: dict[str, Any]) -> list[dict[str, Any]]:
    templates = [
        (
            "understanding",
            "Σε σενάριο όπου {context}, ποια απάντηση δείχνει την πιο κριτική κατανόηση του θέματος;",
            "claim",
            [
                "{misread}",
                "Να μείνει η απάντηση σε ορισμό χωρίς σύνδεση με πλαίσιο και συνέπειες.",
                "Να αντιμετωπιστεί το φαινόμενο ως καθαρά προσωπική ιδιότητα.",
            ],
        ),
        (
            "application",
            "Ποια εφαρμογή του/της {name} αντιμετωπίζει σωστά μια συνηθισμένη εννοιολογική σύγχυση;",
            "application",
            [
                "{misread}",
                "Να αγνοηθεί η διαφορά ανάμεσα σε περιγραφή, εξήγηση και αξιολόγηση.",
                "Να προτιμηθεί η πιο ηθικολογική ερμηνεία επειδή ακούγεται ισχυρότερη.",
            ],
        ),
        (
            "critique",
            "Ποια κριτική ερώτηση πρέπει να τεθεί όταν χρησιμοποιούμε την έννοια {name};",
            "critique",
            [
                "{misread}",
                "Να θεωρηθεί ότι η κριτική είναι απλή απόρριψη της κλασικής θεωρίας.",
                "Να ψάξουμε μόνο αν η έννοια έχει έναν σωστό ορισμό.",
            ],
        ),
        (
            "method",
            "Αν έπρεπε να σχεδιαστεί μικρή έρευνα για το/την {name}, ποια επιλογή είναι πιο ισχυρή;",
            "method",
            [
                "{misread}",
                "Να ρωτήσουμε μόνο τους συμμετέχοντες τι πιστεύουν ότι θα έκαναν.",
                "Να μετρήσουμε ένα αποτέλεσμα χωρίς να αλλάζει συστηματικά το κρίσιμο πλαίσιο.",
            ],
        ),
    ]
    questions: list[dict[str, Any]] = []
    counter = 1
    method_bonus = 2 if len(topic["concepts"]) > 6 else 1
    for seed_index, seed in enumerate(topic["concepts"]):
        for template_index, (kind, stem_template, correct_key, distractor_templates) in enumerate(templates):
            if kind == "method" and seed_index >= method_bonus:
                continue
            correct = seed[correct_key]
            distractors = [
                template.format(**seed)
                for template in distractor_templates
            ]
            options = [correct, *distractors]
            shift = (counter + len(topic["slug"]) + template_index) % 4
            ordered = options[shift:] + options[:shift]
            answer_index = ordered.index(correct)
            answer_letter = ["Α", "Β", "Γ", "Δ"][answer_index]
            questions.append(
                {
                    "id": f"{topic['slug']}-q{counter:02d}",
                    "topic": topic["title"],
                    "question": stem_template.format(**seed),
                    "options": ordered,
                    "answer": answer_letter,
                    "explanation": (
                        f"Σωστή είναι η {answer_letter}. {correct} "
                        f"Το βασικό σκεπτικό είναι να αποφευχθεί η υπεραπλούστευση: {seed['misread']}"
                    ),
                    "source": seed["source"],
                    "concept": seed["name"],
                }
            )
            counter += 1
    return questions


